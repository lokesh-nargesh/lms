    private void buttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSearchActionPerformed
        String bookID = bookidtxt.getText();
        String studentID = studentidtxt.getText();
        try {
            Connection conn = ConnectionProvider.getCon();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from issue where bookID = '"+bookID+"' and studentID = '"+studentID+"'");
            if (rs.next()) {
                issueDatetxt.setText(rs.getString(3));
                dueDatetxt.setText(rs.getString(4));
                bookidtxt.setEditable(false);
                studentidtxt.setEditable(false);
            } else {
                JOptionPane.showConfirmDialog(null, "Book is not Issued to this Student");
                setVisible(false);
                new returnBook().setVisible(true);
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Book Error");
            setVisible(false);
            new returnBook().setVisible(true);
        }
    }//GEN-LAST:event_buttonSearchActionPerformed

    private void buttonReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonReturnActionPerformed
        String bookID = bookidtxt.getText();
        String studentID = studentidtxt.getText();
        try {
            Connection conn = ConnectionProvider.getCon();
            Statement st = conn.createStatement();
            st.executeUpdate("update issue set returnBook = 'Yes' where bookID = '"+bookID+"' and studentID = '"+studentID+"'");
            JOptionPane.showMessageDialog(null, "Book Succesfully Returned");
            setVisible(false);
            new returnBook().setVisible(true);
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Book Error");
            setVisible(false);
            new returnBook().setVisible(true);
        }
    }//GEN-LAST:event_buttonReturnActionPerformed

