    private void buttonIssueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonIssueActionPerformed
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-mm-yyyy");
        String bookID = studentidtxt.getText();
        String studentID = bookidtxt.getText();
        String issueDate = dFormat.format(issueDatetxt.getDate());
        String dueDate = dFormat.format(dueDatetxt.getDate());
        String returnBook = "No";
        try {
            Connection conn = ConnectionProvider.getCon();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from book where bookID = '"+bookID+"'");
            if (rs.next()) {
                ResultSet rs1 = st.executeQuery("select * from student where studentID = '"+studentID+"'");
                if (rs1.next()) {
                    st.executeUpdate("insert into issue values('"+bookID+"', '"+studentID+"', '"+issueDate+"', '"+dueDate+"', '"+returnBook+"')");
                    JOptionPane.showConfirmDialog(null, "Book Successfully Issued");
                    setVisible(false);
                    new issueBook().setVisible(true);
                } else {
                    JOptionPane.showConfirmDialog(null, "Incorrect Student ID");
                }
            } else {
                JOptionPane.showConfirmDialog(null, "Incorrect Book ID");
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Book Error");
            setVisible(false);
            new issueBook().setVisible(true);
        }
    }//GEN-LAST:event_buttonIssueActionPerformed

